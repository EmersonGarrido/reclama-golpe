async function seedCategories() {
  console.log('üå± Iniciando seed de categorias...')
  
  for (const category of categories) {
    const existing = await prisma.category.findUnique({
      where: { slug: category.slug }
    })

    if (existing) {
      console.log(`‚úì Categoria "${category.name}" j√° existe, atualizando...`)
      await prisma.category.update({
        where: { slug: category.slug },
        data: category
      })
    } else {
      console.log(`+ Criando categoria "${category.name}"...`)
      await prisma.category.create({
        data: category
      })
    }
  }

  const totalCategories = await prisma.category.count()
  console.log(`\n‚úÖ Seed conclu√≠do! Total de categorias: ${totalCategories}`)
}

seedCategories()
